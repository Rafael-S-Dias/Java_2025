    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        String placaBusca = txtBuscar.getText().trim();
        String novaMarca = txtMarca.getText().trim();
        String novoModelo = txtModelo.getText().trim();
        String novaPlaca = txtPlaca.getText().trim();
        String novaPortaTexto = txtPortas.getText().trim();
        
        if (placaBusca.isEmpty() || novaMarca.isEmpty() || novoModelo.isEmpty() || novaPlaca.isEmpty() || novaPortaTexto.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
            return;
        }
        
        try {
            int novaPorta = Integer.parseInt(novaPortaTexto);
            boolean encontrado = false;
           
            for (Carro c : listaCarros) {
                if (c.getPlaca().equalsIgnoreCase(placaBusca)) {
                    c.setMarca(novaMarca);
                    c.setModelo(novoModelo);
                    c.setPlaca(novaPlaca);
                    c.setPortas(novaPorta);
                    encontrado = true;
                    break;
                }
            }
            
            if (encontrado) {
                JOptionPane.showMessageDialog(null, "Dados atualizados com sucesso!");
                txtArea.setText("");
                for (Carro c : listaCarros) {
                        txtArea.append(c.toString()+ "\n");
                    }
                } else {
                  JOptionPane.showMessageDialog(null, "Carro não encontrado!");
                }
            } catch (NumberFormatException e ) {
                JOptionPane.showMessageDialog(null, "Dados inválidos!");
            }
    }//GEN-LAST:event_btnAlterarActionPerformed

