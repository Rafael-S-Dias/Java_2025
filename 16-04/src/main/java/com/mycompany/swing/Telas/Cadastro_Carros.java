    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        String placaBusca = txtBuscar.getText().trim();
        
        if (placaBusca.isEmpty()){
            JOptionPane.showMessageDialog(null, "Digite a placa para buscar: ");
            return;
        }
        
        boolean removido = false;
        
        for (int i = 0; i < listaCarros.size(); i++) {
            if (listaCarros.get(i).getPlaca().equalsIgnoreCase(placaBusca)){
                listaCarros.remove(i);
                removido = true;
                break;
            }
        }
        
        if (removido) {
            JOptionPane.showMessageDialog(null, "Carro removido com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Carro não encontrado!");
        }
        
        txtArea.setText("");
        for (Carro c : listaCarros) {
            txtArea.append(c.toString() + "\n");
        }
        
        txtMarca.setText("");
        txtModelo.setText("");
        txtPlaca.setText("");
        txtPortas.setText("");
        txtBuscar.setText("");
    }//GEN-LAST:event_btnRemoverActionPerformed

