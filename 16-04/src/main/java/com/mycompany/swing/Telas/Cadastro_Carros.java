    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        try{
            String marca = txtMarca.getText();
            String modelo = txtModelo.getText();
            String placa = txtPlaca.getText();
            String portasTexto = txtPortas.getText();
            int portas = Integer.parseInt(portasTexto);

            Carro c = new Carro(portas, marca, modelo, placa);
            listaCarros.add(c);
            txtArea.append(c.toString() + "\n");

            txtMarca.setText("");
            txtModelo.setText("");
            txtPlaca.setText("");
            txtPortas.setText("");
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Dados inválido");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        // TODO add your handling code here:
        String placaBusca = txtBuscar.getText().trim();
        
        if (placaBusca.isEmpty()){
            JOptionPane.showMessageDialog(null, "Digite a placa para buscar: ");
            return;
        }
        
        boolean encontrado = false;
        
        for (Carro c : listaCarros) {
            if (c.getPlaca().equalsIgnoreCase(placaBusca)) {
                txtMarca.setText(c.getMarca());
                txtModelo.setText(c.getModelo());
                txtPlaca.setText(c.getPlaca());
                txtPortas.setText(String.valueOf(c.getPortas()));
                encontrado = true;
                break;
            }
        }
        
        if (!encontrado) {
            JOptionPane.showMessageDialog(null, "Placa não encontrada.");
            txtMarca.setText("");
            txtModelo.setText("");
            txtPlaca.setText("");
            txtPortas.setText("");
            
        }
    }//GEN-LAST:event_btnListarActionPerformed

