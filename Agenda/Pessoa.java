public class Pessoa {

    private String nome;
    private String telefone;

    public Pessoa(String nome, String telefone){
        this.nome = nome;
        this.telefone = telefone;
    }

    public String getNome(){
        return nome;
 
    }
    public String setNome(){
        return nome;
 
    }

    public String getTelefone(){
        return telefone;

    }
    public String setTelefone(){
        return telefone;

    }
}