    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        // TODO add your handling code here:
        String nome = txtNome.getText().trim();
        String especie = txtEspecie.getText().trim();
        int idade;

    try {
        idade = Integer.parseInt(txtIdade.getText().trim());

        if (!nome.isEmpty() || !especie.isEmpty()) {
            if (!modoAtualizacao) {
                controle.cadastrar(nome, especie, idade);
                JOptionPane.showMessageDialog(this, "Cadastro realizado com sucesso!");
            } else {
                controle.atualizar(idSelecionado, nome, especie, idade);
                JOptionPane.showMessageDialog(this, "Atualização realizada com sucesso!");
                btnEntrar.setText("Cadastrar");
                idSelecionado = -1;
                modoAtualizacao = false;
            }

            atualizarTabela();
            txtNome.setText("");
            txtEspecie.setText("");
            txtIdade.setText("");

        } else {
            JOptionPane.showMessageDialog(this, "Preencha os campos corretamente.");
        }

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Idade deve ser um número inteiro.");
    }
    }//GEN-LAST:event_btnEntrarActionPerformed

