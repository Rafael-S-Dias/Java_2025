    private void atualizarTabela() {
    tabelaModelo.setRowCount(0); // limpa os dados da tabela

    ArrayList<Animal> lista = controle.listar(); // busca do banco
    for (Animal a : lista) {
        tabelaModelo.addRow(new Object[]{a.getId(), a.getNome(), a.getEspecie(), a.getIdade()});
    }
}
   private void buscarDinamicamente() {
    String nomeBuscado = txtBusca.getText().trim();

    tabelaModelo.setRowCount(0); // limpa a tabela
    ArrayList<Animal> lista = controle.buscarPorNome(nomeBuscado);

    for (Animal a : lista) {
        tabelaModelo.addRow(new Object[]{a.getId(), a.getNome(), a.getEspecie(), a.getIdade()});
    }
}
    
